package fundamentos;

public class OperadoresAtribuicao {
    public static void main(String args[]){
        int a = 3; //atribuição de valores
        System.out.println(a);
        a += 1; //a = a + 1;
        System.out.println(a);
        a -= 2; // a = a - 2;
        System.out.println(a);
        a *=1024; //a = a * 1024
        System.out.println(a);
        a/=16; //a = a / 16
        System.out.println(a);
        a%=2; // a = a % 16
        System.out.println(a);
    }
    
}
