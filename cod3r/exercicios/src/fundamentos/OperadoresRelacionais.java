package fundamentos;

public class OperadoresRelacionais{
    public static void main(String args[]){
        int a = 97;
        int b = 'a';
        int c = 12;

        System.out.println(a == b);
        //cada caracter tem um valor agregado

        System.out.println(55 > 10);
        System.out.println(99 < 101);
        System.out.println(3 >= -19);
        System.out.println(-16 <= -16);
        System.out.println(12 != c);
        System.out.println(97 == a);

    }
}