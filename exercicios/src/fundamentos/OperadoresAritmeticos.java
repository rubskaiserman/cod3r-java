package fundamentos;

public class OperadoresAritmeticos {
    public static void main(String[] args) {
        double x = 19.287;
        double y = 16.5567;

        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);
        System.out.println(x % y);

    }
}
